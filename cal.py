a=int(input('enter the number a'))
b=int(input('enter the number b'))
enter=input('enter the (add,sub,mul,div,mod)')
if enter=='add':
    c=a+b
    print(c)
elif enter=='sub':
    c=a-b
    print(c)
elif enter=='mul':
    c=a*b
    print(c)
elif enter=='div':
    c=a/b
    print(c)
elif enter=='mod':
    c=a%b
    print(c)
